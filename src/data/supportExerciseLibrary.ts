export const SUPPORT_EXERCISE_MAP = Object.fromEntries(SUPPORT_EXERCISE_LIBRARY.map((exercise) => [exercise.id, exercise]));

export const validateSupportExerciseReferences = (moduleGroups: Array<{ id: string; exercises: Array<{ exerciseId: string }> }>) => {
  const missing: string[] = [];

  moduleGroups.forEach((module) => {
    module.exercises.forEach((exercise) => {
      if (!SUPPORT_EXERCISE_MAP[exercise.exerciseId]) {
        missing.push(`${module.id}:${exercise.exerciseId}`);
      }
    });
  });

  if (missing.length) {
    console.warn('IronPath support exercise reference warnings:', missing.join(', '));
  }

  return missing;
};
